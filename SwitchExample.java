package evenodd;

public class SwitchExample {
	public static void main(String[] args)
	{
		int value=2;
		switch(value) {
		case 1:
			System.out.println("HEY");
			 break;
		case 2:
			System.out.println("HELLO");
			break;
		case 3:
			System.out.println("BYE");
			break;
		case 4:
			System.out.println("DONE");
			break;
		default:
			System.out.println("BYE BYE");
			break;
		}
	}

}
